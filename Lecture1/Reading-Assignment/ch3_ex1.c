#include <stdio.h>

int main(void)
{
    /* Declaring variables to types int, float */
    int i, j;
    float x, y;

    /* Assigning variables */
    i = 10;
    j = 20;
    x = 43.2892f;
    y = 5527.0f;

    /* Printing output
        %d = int
        %f = float*/
    printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y);
}
